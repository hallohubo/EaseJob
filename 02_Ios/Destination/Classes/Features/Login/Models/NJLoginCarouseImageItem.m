#import "NJLoginCarouseImageItem.h"
#define ImageViewWidth (HDScreenW * 0.5)

@implementation NJLoginCarouseImageItem
- (void)setCarouseImage:(UIImage *)carouseImage
{
    _carouseImage = carouseImage;
    CGSize imageSize = carouseImage.size;
    _imageViewWidth = ImageViewWidth;
    _imageViewHeight = _imageViewWidth * imageSize.height / imageSize.width;
}
@end
